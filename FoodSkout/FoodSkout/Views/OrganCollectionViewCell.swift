import UIKit

class OrganCollectionViewCell: UICollectionViewCell {
    
    @IBOutlet weak var organNameLabel: UILabel!
    @IBOutlet weak var organImg: UIImageView!
    
    func configureCell(collectionImg: UIImage, organName: String) {
        self.organImg.image = collectionImg
        self.organNameLabel.text = organName
    }
}
